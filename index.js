function add(a, b) {
  return (a + b)
}

function subtract(a, b) {
  return (a - b)
}

function multiply(a, b) {
  return (a * b)
}

function divide(a, b) {
  return (a / b)
}

function makeInt(n) {
  parseInt('n', 10);
  return 'n';
}

function preserveDecimal(n) {
  parseFloat('n');
  return 'n';
}

